<script>
    document.addEventListener("DOMContentLoaded", function () {
        document.querySelectorAll(".Add-more").forEach(button => {
            button.addEventListener("click", function (event) {
                event.preventDefault();

                let section = this.closest(".box-primary");
                let firstFieldset = section.querySelector(".box-body .row");
                let newFieldset = firstFieldset.cloneNode(true);

                newFieldset.querySelectorAll("input, textarea").forEach(input => {
                    if (input.type === "checkbox") {
                        input.checked = false;
                    } else {
                        input.value = "";
                    }
                });

                newFieldset.querySelectorAll("input[type='checkbox']").forEach(input => {
                    let newId = input.id + "_copy_" + Date.now();
                    input.id = newId;
                    let label = newFieldset.querySelector(`label[for="${input.id}"]`);
                    if (label) {
                        label.setAttribute("for", newId);
                    }
                });

                let wrapper = document.createElement("div");
                wrapper.style.border = "1px solid #ccc";
                wrapper.style.borderRadius = "5px";
                wrapper.style.padding = "15px";
                wrapper.style.marginTop = "10px";
                wrapper.style.position = "relative";
                wrapper.style.backgroundColor = "#f9f9f9";
                wrapper.classList.add("row");

                let removeBtn = document.createElement("button");
                removeBtn.innerHTML = "âœ–";
                removeBtn.style.position = "absolute";
                removeBtn.style.top = "5px";
                removeBtn.style.right = "10px";
                removeBtn.style.border = "none";
                removeBtn.style.background = "red";
                removeBtn.style.color = "white";
                removeBtn.style.padding = "2px 6px";
                removeBtn.style.borderRadius = "50%";
                removeBtn.style.cursor = "pointer";

                removeBtn.addEventListener("click", function () {
                    wrapper.remove();
                });

                wrapper.appendChild(removeBtn);
                wrapper.appendChild(newFieldset);

                firstFieldset.parentNode.appendChild(wrapper);
            });
        });

        document.querySelectorAll(".Add-more").forEach(button => {
            button.style.float = "right";
        });
    });
</script>